#! /usr/bin/env Rscript
library(optparse)
library(tools)

option_list <- list(
    make_option(c("--metaxcan_dir"), type="character", default=NULL,
                help="The prefix of all the Metaxcan results",
                metavar="character"),
    make_option(c("--smultixcan_file"), type="character", default=NULL,
                 help="Summary multixcan results for genes",
                 metavar="character"),
    make_option(c("--smetaboxcan_file"),type = "character", default = NULL,
                help = "Summary metaboxcan results for best metabolite models",
                metavar = "char"),
    make_option(c("--predict_db"),type = "character", default = NULL,
                help = "The database containing the metabolite models",
                metavar = "char"),
    make_option(c("--gene_metabo_assoc_file"),type = "character", default = NULL,
                help = "A gene-metabolite association file",
                metavar = "char"),
    make_option(c("--gene_annotation"),type = "character", default = NULL,
                help = "The gene annotation file to use",
                metavar = "character"),
    make_option(c("--mqtl_db"),type = "character", default = NULL,
                help = "The db containing the metabolite qtls",
                metavar = "character"),
    make_option(c("--omics_network"), type="character", default=NULL,
                help="The larger biological network used to find sub-networks",
                metavar="character"),
    make_option(c("--gene_info"), type="character", default=NULL,
                help="A file containing more details about a gene to be used in network annot",
                metavar="character"),
    make_option(c("--metabo_pathways"),type = "character", default = NULL,
                help = "A summary of pathways for the metabolites",
                metavar = "character"),
    make_option(c("--ld_blocks"),type = "character", default = NULL,
                help = "The ld blocks file",
                metavar = "character"),
    make_option(c("--liftover_chain"),type = "character", default = NULL,
                help = "The liftover chain to use for mqtls",
                metavar = "character"),
    make_option(c("--color_code_yaml"), type="character", default=NULL,
                help="Color coding",
                metavar="character"),
    make_option(c("--rlib"), type="character", default=NULL,
                help="The path to report helper functions",
                metavar="character"),
    make_option(c("--ntop"), type="numeric", default=20,
                help="Number of top metabo and gene associations to show",
                metavar="character"),
    make_option(c("--phenotype_name"), type="character", default=NULL,
                help="Phenotype name to show in the report",
                metavar="character"),
    make_option(c("--prefix"), type="character", default=NULL,
                help="Prefix name used in the analysis",
                metavar="character"),
    make_option(c("--gwas_file"), type="character", default=NULL,
                help="The gwas file you are running in a .db format",
                metavar="character"),
    make_option(c("--rmd_template"), type="character", default=NULL,
                help="R Markdown template",
                metavar="character"),
    make_option(c("--output_html"), type="character", default=NULL,
                help="Output HTML filename",
                metavar="character")
)

opt_parser <- OptionParser(option_list=option_list)
opt <- parse_args(opt_parser)

params = list(
  color_code_yaml = NA, #opt$color_code_yaml,
  metaxcan_dir = file_path_as_absolute(opt$metaxcan_dir),
  smultixcan_file = file_path_as_absolute(opt$smultixcan_file),
  smetaboxcan_file = file_path_as_absolute(opt$smetaboxcan_file),
  predict_db = file_path_as_absolute(opt$predict_db),
  gene_metabolite_assoc_file = file_path_as_absolute(opt$gene_metabo_assoc_file),
  summary_stat_file = file_path_as_absolute(opt$gwas_file),
  gene_annotation = file_path_as_absolute(opt$gene_annotation),
  mqtl_db = file_path_as_absolute(opt$mqtl_db),
  metabo_pathways = file_path_as_absolute(opt$metabo_pathways),
  ld_blocks = file_path_as_absolute(opt$ld_blocks),
  liftover_chain = (opt$liftover_chain),
  rlib = NA,#opt$rlib,
  phenotype_name = opt$phenotype_name,
  prefix = opt$prefix,
  ntop = opt$ntop,
  omics_network = file_path_as_absolute(opt$omics_network),
  gene_info = file_path_as_absolute(opt$gene_info),
  output_dir = normalizePath(dirname(opt$output_html), mustWork = FALSE)
)

rmarkdown::render(
  opt$rmd_template,
  params = params,
  envir = new.env(),
  encoding = "UTF-8",
  output_dir = dirname(opt$output_html),
  output_file = basename(opt$output_html)
)
